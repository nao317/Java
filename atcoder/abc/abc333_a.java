import java.util.*;

public class abc333_a {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int n = scan.nextInt();
        for (int i = 0; i < n; i++) {
            System.out.printf("%d", n);
        }
        System.out.printf("\n");
    }
}